public class Hexagon {
		public static void top() {
			System.out.println("  _______");
			System.out.println(" /       \\");
			System.out.println("/         \\");
		}
		public static void middle() {
			System.out.println("_\"_'_\"_'_\"_");
		}
		public static void bottom() {
			System.out.println("\\         /");
			System.out.println(" \\_______/");
		}

	public static void main (String[] args) {
		top();
		middle();
		bottom();
	}
}